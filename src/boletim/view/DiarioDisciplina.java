    public void readTableDisc(){
            
        DefaultTableModel modelo = (DefaultTableModel) jTableAluno.getModel();
            //modelo.addRow(new String [] {});
        try{
            DAOalunos alunos = new DAOalunos();
        
                alunos.read();
        
            for(Aluno aluno: alunos.read()){
                modelo.addRow(new Object[]{
                    aluno.getMatricula(),
                    aluno.getNome(),
                    aluno.getStatus()
                });
             }
        }catch(Exception ex){
            
            JOptionPane.showMessageDialog(null, "Erro na Listagem "+ex);
            
        }
    }
    
